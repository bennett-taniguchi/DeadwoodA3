public class UpgradeArea {
    public int x;
    public int y;
    public int h;
    public int w;

    public UpgradeArea(){
        x = 0;
        y = 0;
        h = 0;
        w = 0;
    }

    public UpgradeArea(int x, int y, int h, int w){
        this.x = x;
        this.y = y;
        this.h = h;
        this.w = w; 
    }
}
