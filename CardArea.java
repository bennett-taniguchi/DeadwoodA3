public class CardArea {
    public int x;
    public int y;
    public int h;
    public int w;

    public CardArea(){
        x = 0;
        y = 0;
        h = 0;
        w = 0;
    }

    public CardArea(int x, int y, int h, int w){
        this.x = x;
        this.y = y;
        this.h = h;
        this.w = w;
    }

}
