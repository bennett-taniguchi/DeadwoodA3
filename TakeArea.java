public class TakeArea {
    public int x;
    public int y;
    public int h;
    public int w;
    public int num;

    public TakeArea(){
        x = 0;
        y = 0;
        h = 0;
        w = 0;
        num=0;
    }

    public TakeArea(int x, int y, int h, int w, int num){
        this.x = x;
        this.y = y;
        this.h = h;
        this.w = w;
        this.num = num;
    }
}
